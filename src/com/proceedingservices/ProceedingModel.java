package com.proceedingservices;

public class ProceedingModel {
	
	public String pc_formname = "";
	public String pc_formguid = "";
	public String pc_formgroup = "";
	public String pc_formgroupcode = "";
	
	public ProceedingModel(){}

	public String getPc_formname() {
		return pc_formname;
	}

	public void setPc_formname(String pc_formname) {
		this.pc_formname = pc_formname;
	}

	public String getPc_formguid() {
		return pc_formguid;
	}

	public void setPc_formguid(String pc_formguid) {
		this.pc_formguid = pc_formguid;
	}

	public String getPc_formgroup() {
		return pc_formgroup;
	}

	public void setPc_formgroup(String pc_formgroup) {
		this.pc_formgroup = pc_formgroup;
	}

	public String getPc_formgroupcode() {
		return pc_formgroupcode;
	}

	public void setPc_formgroupcode(String pc_formgroupcode) {
		this.pc_formgroupcode = pc_formgroupcode;
	}
	

}
